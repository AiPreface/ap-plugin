async function Translate (text, lang) {
  const qs = (obj) =>
    Object.entries(obj)
      .map(([k, v]) => `${k}=${encodeURIComponent(v)}`)
      .join('&')
  const appVersion = '5.0 (Windows NT 10.0; Win64; x64) Chrome/98.0.4750.0'
  const headers = {
    Host: 'fanyi.youdao.com',
    'User-Agent':
      'Mozilla/5.0 (Windows NT 10.0; Win64; x64) Chrome/98.0.4758.102',
    Referer: 'https://fanyi.youdao.com/',
    'Content-Type': 'application/x-www-form-urlencoded; charset=UTF-8',
    Cookie:
      'OUTFOX_SEARCH_USER_ID_NCOO=133190305.98519628; OUTFOX_SEARCH_USER_ID="2081065877@10.169.0.102";'
  }
  const api =
    'https://fanyi.youdao.com/translate_o?smartresult=dict&smartresult=rule'
  const key = 'Ygy_4c=r#e#4EX^NUGUc5'
  const lts = Date.now().toString()
  const salt = `${lts}${Math.floor(10 * Math.random())}`
  const sign = md5(`fanyideskweb${text}${salt}${key}`)
  const data = {
    i: text,
    from: 'AUTO',
    to: lang,
    smartresult: 'dict',
    client: 'fanyideskweb',
    salt,
    sign,
    lts,
    bv: md5(appVersion),
    doctype: 'json',
    version: '2.1',
    keyfrom: 'fanyi.web',
    action: 'FY_BY_DEFAULT'
  }
  try {
    const res = await fetch(api, {
      method: 'POST',
      body: qs(data),
      headers
    })
    const { errorCode, translateResult } = await res.json()
    if (errorCode !== 0) {
      return false
    }
    const translated = lodash
      .flattenDeep(translateResult)
      ?.map((item) => item.tgt)
      .join('\n')
    return translated || false
  } catch (e) {
    Log.e('【椰奶有道翻译报错】:', e)
    return false
  }
}
